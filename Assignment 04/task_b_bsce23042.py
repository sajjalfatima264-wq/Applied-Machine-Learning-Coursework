def compute_roc(yt,ys,n=300):
    tprs,fprs=[],[]
    for th in np.linspace(0,1,n):
        p=(ys>=th).astype(int)
        tp=np.sum((yt==1)&(p==1)); fn=np.sum((yt==1)&(p==0))
        fp=np.sum((yt==0)&(p==1)); tn=np.sum((yt==0)&(p==0))
        tprs.append(tp/(tp+fn+1e-8)); fprs.append(fp/(fp+tn+1e-8))
    return np.array(fprs),np.array(tprs)

